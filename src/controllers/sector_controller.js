export const Getsector = async (req, res) => {
  try {
    const { id } = req.params;
    const idfound = await sector.findOne( {_id:id});
    if(!idfound) return res.status(406).json({message:"sector no encontrado"})
    return res.status(200).json({message:"sector encontrado correctamente",sector:idfound})
  } catch (error) {
    return res.status(500).json({message:"Error al encontrar sector"})
  }
}
