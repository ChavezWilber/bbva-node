export const  createsector = async (req, res) => {
  try {
    console.log(req.body)
    const { cd_sector,nm_sector} = req.body
    const newsector = new sector({cd_sector,nm_sector});
    
    const savedsector = await newsector.save();
    
    return res.status(200).json({  sector:savedsector, message:"sector creado correctamente",status:200 });
  } catch (error) {
  
    console.log(error)
    return res.status(200).json({message:"Campos imcompletos",status:500})
  }
};
